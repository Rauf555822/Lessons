package org.example.ders65;

public class Telebe {
    int id;
    String name;
    int grade;

    public Telebe(int id, String name, int grade) {
        this.id = id;
        this.name = name;
        this.grade = grade;
    }

    public String toString() {
        return "Student [id=" + id + ", name=" + name + ", grade=" + grade + "]";
    }
}


